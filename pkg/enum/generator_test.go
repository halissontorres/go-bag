func TestGenerate_WithOutputSuffix(t *testing.T) {
	t.Parallel()
	dir := t.TempDir()
	writeFile(t, dir, "color.go", colorSrc)

	if err := Generate(dir, "Color", WithOutputSuffix("_gen.go")); err != nil {
		t.Fatalf("Generate: %v", err)
	}

	// Arquivo com sufixo customizado deve existir.
	if _, err := os.Stat(filepath.Join(dir, "color_gen.go")); err != nil {
		t.Fatalf("expected color_gen.go, got: %v", err)
	}

	// Arquivo com sufixo padrão não deve existir.
	if _, err := os.Stat(filepath.Join(dir, "color_enum.gen.go")); err == nil {
		t.Fatal("color_enum.gen.go should not exist when suffix is overridden")
	}
}

func TestGenerate_WithOutputSuffix_SkipsOwnOutput(t *testing.T) {
	t.Parallel()
	dir := t.TempDir()
	writeFile(t, dir, "color.go", colorSrc)

	// Arquivo gerado anteriormente com sufixo customizado deve ser ignorado no re-parse.
	writeFile(t, dir, "color_gen.go", `// Code generated by Typed Enum Generator; DO NOT EDIT.
package mypkg
`)
	if err := Generate(dir, "Color", WithOutputSuffix("_gen.go")); err != nil {
		t.Fatalf("Generate on re-run: %v", err)
	}
}

// ---- helpers ----

func readGenerated(t *testing.T, dir, name string) string {
	t.Helper()
	out, err := os.ReadFile(filepath.Join(dir, name))
	if err != nil {
		t.Fatalf("read generated file %s: %v", name, err)
	}
	return string(out)
}
