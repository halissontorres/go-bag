func TestGenerate_NoJSONNoSQL_NoSuperfluousImports(t *testing.T) {
	t.Parallel()
	dir := t.TempDir()
	writeFile(t, dir, "color.go", colorSrc)

	if err := Generate(dir, "Color", WithJSON(false), WithSQL(false)); err != nil {
		t.Fatalf("Generate: %v", err)
	}

	src := readGenerated(t, dir, "color_enum.gen.go")

	for _, absent := range []string{
		"encoding/json",
		"database/sql/driver",
	} {
		if strings.Contains(src, absent) {
			t.Errorf("generated code should not contain import %q", absent)
		}
	}

	// fmt ainda deve estar presente por causa de ParseColor.
	if !strings.Contains(src, `"fmt"`) {
		t.Error("generated code should still import fmt (needed by ParseColor)")
	}
}

