func TestGenerate_SkipsPreviouslyGenerated(t *testing.T) {
	t.Parallel()

	dir := t.TempDir()
	writeFile(t, dir, "status.go", `package mypkg

type Status string

const (
	Active   Status = "active"
	Inactive Status = "inactive"
)
`)
	// A previously-generated file should be ignored on subsequent runs to avoid
	// the parser tripping on duplicate methods.
	writeFile(t, dir, "status_enum.gen.go", `// Code generated by Typed Enum Generator; DO NOT EDIT.
package mypkg
`)

	if err := Generate(dir, "Status"); err != nil {
		t.Fatalf("Generate: %v", err)
	}
	if _, err := os.Stat(filepath.Join(dir, "status_enum.gen.go")); err != nil {
		t.Fatalf("generated file missing: %v", err)
	}
}

const colorSrc = `package mypkg
type Color string
const (
	Red   Color = "red"
	Green Color = "green"
	Blue  Color = "blue"
)
`

func TestGenerate_WithJSON_False(t *testing.T) {
	t.Parallel()
	dir := t.TempDir()
	writeFile(t, dir, "color.go", colorSrc)

	if err := Generate(dir, "Color", WithJSON(false)); err != nil {
		t.Fatalf("Generate: %v", err)
	}

	src := readGenerated(t, dir, "color_enum.gen.go")

	// JSON methods must be absent.
	for _, absent := range []string{
		"encoding/json",
		"func (t Color) MarshalJSON()",
		"func (t *Color) UnmarshalJSON(",
	} {
		if strings.Contains(src, absent) {
			t.Errorf("generated code should not contain %q", absent)
		}
	}

	// Core methods must still be present.
	for _, want := range []string{
		"func (t Color) IsValid() bool",
		"func ParseColor(s string)",
	} {
		if !strings.Contains(src, want) {
			t.Errorf("generated code missing %q", want)
		}
	}
}

